#TO SKIP LINE AND GO TO NEXT LINE = '\n'
print( "mayur" "\n" "kumar\n" "g" )

#TO SEPARATE THE STATEMENT WITH ANY STRING OR CHARACTER = sep=" "
print("mayur" "hgwsdg", 3,4,6,"hdfgh", "grsg", sep=" ~~~~~ ")

#TO PRINT ANYTHING AT THE END BEFORE PRINTING THE NEXT STATEMENT = end="  "
# The end="" argument is used with the print() function to specify the character or characters that should be
# printed at the end of the output instead of the default newline character (\n).
print("ghjtgu",334,"hjksd", end="AAAA\n")
print("vgdscgjhsdg")